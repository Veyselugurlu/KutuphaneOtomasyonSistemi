    private void BSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BSilActionPerformed
         String labelYayineviId = LYayinevid.getText();
        
        
        if(labelYayineviId.equals("")){
            JOptionPane.showMessageDialog(this, "Lütfen Bir Yayınevi Seçiniz!");

        }else{
            
            int yayineviId = Integer.parseInt(LYayinevid.getText());
            int sonuc = JOptionPane.showConfirmDialog(null, "Yayınevini Silmek İstediğinize Emin misiniz?", "Yayınevi Silme", JOptionPane.YES_NO_CANCEL_OPTION);

            if (sonuc == JOptionPane.YES_OPTION) {
            
                if(ki.yayineviSil(yayineviId) > 0){
                    JOptionPane.showMessageDialog(this, "Yayınevi Başarıyla Silindi.");
                    
                    PersonelAnasayfa ana= new PersonelAnasayfa();
                    ana.setVisible(true);
                    this.dispose();
                }
            else{
                JOptionPane.showMessageDialog(this, "Silinirken Bir Hata Meydana Geldi.");
            }
                  
           
            } 
        }
    }//GEN-LAST:event_BSilActionPerformed

