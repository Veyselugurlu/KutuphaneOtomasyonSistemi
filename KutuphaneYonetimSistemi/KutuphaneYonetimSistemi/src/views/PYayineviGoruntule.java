    public void yayinevleriniGoruntule()
    {
        model.setRowCount(0);
        ArrayList<YayineviModel> yayinevleri = new ArrayList<YayineviModel>();

        yayinevleri = ki.yayinevleriniGoruntule();
        
        if (yayinevleri != null)
        {
            for (YayineviModel yayinevi : yayinevleri)
            {
                Object[] add = {
                    yayinevi.getYayineviId(),yayinevi.getYayineviAdi(),yayinevi.getYayineviKurulusTarihi(),yayinevi.getYayineviTelNo(),
                    yayinevi.getYayineviAdresi(),yayinevi.getYayineviMail()
                };
                
                model.addRow(add);         
            }
            
        }
        
        
    }
    
    private void search() {
        String searchTerm = searchField.getText().toLowerCase();
        TableRowSorter<DefaultTableModel> rowSorter = new TableRowSorter<>(model);
        TKitaplar.setRowSorter(rowSorter);

        int columnIndexToSearch = 1;

        if (searchTerm.length() == 0) {
            rowSorter.setRowFilter(null);
        } else {
            rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + searchTerm, columnIndexToSearch));
        }
    }

