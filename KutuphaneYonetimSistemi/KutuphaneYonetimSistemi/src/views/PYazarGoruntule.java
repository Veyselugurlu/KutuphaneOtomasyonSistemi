    private void BGuncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BGuncelleActionPerformed
        
        String labelYazarid = LYazarid.getText();
        String yazarAdi = TFYazarAd.getText();
        String yazarSoyad = TFYazarSoyad.getText();
        java.util.Date dogumTarihi = DCYDogumTarihi.getDate();
        String dogumYeri = TFYDogumYeri.getText();
        int cinsiyet = CBYCinsiyet.getSelectedIndex();
        String aciklama = TAYAciklama.getText();
        
        if(yazarAdi.equals("") || yazarSoyad.equals("") || dogumYeri.equals("") || aciklama.equals("") || 
           labelYazarid.equals("") || dogumTarihi == null){
            JOptionPane.showMessageDialog(this, "Lütfen Tüm Boşlukları Doldurun!");

        }
        else{
            
            int yazarid = Integer.parseInt(LYazarid.getText());
           
            KitapYazariFactory kitapYazariFactory = new KitapYazariFactory();
            KitapYazari kitapYazari = (KitapYazari) kitapYazariFactory.createYazar();
            kitapYazari.YazarBilgileriniDuzenleIDli(yazarid,yazarAdi, yazarSoyad, dogumTarihi, dogumYeri, cinsiyet, aciklama);
                
            
            if(ki.yazarGuncelle(kitapYazari) > 0 ){
                JOptionPane.showMessageDialog(this, "Yazar Başarıyla Güncellendi.");
                PersonelAnasayfa pAnasayfa = new PersonelAnasayfa();
                pAnasayfa.setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "Güncellenirken Bir Hata Meydana Geldi.");
            }
        }
        
       
    }//GEN-LAST:event_BGuncelleActionPerformed

    private void BSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BSilActionPerformed
        String labelYazarid = LYazarid.getText();

        if(labelYazarid.equals("")){
            JOptionPane.showMessageDialog(this, "Lütfen Bir Yazar Seçiniz!");

        }else{

            int yazarid = Integer.parseInt(LYazarid.getText());
            int sonuc = JOptionPane.showConfirmDialog(null, "Yazarı Silmek İstediğinize Emin misiniz?", "Yazar Silme", JOptionPane.YES_NO_CANCEL_OPTION);

            if (sonuc == JOptionPane.YES_OPTION) {

                if(ki.yazarSil(yazarid) > 0){
                    JOptionPane.showMessageDialog(this, "Yazar Başarıyla Silindi.");
                    PersonelAnasayfa pAnasayfa = new PersonelAnasayfa();
                pAnasayfa.setVisible(true);
                this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(this, "Silinirken Bir Hata Meydana Geldi.");
                }

            }
        }

    }//GEN-LAST:event_BSilActionPerformed

