     public void yazarGoruntule()
    {
        model.setRowCount(0);
        ArrayList<KitapYazari> yazarlar = new ArrayList<KitapYazari>();

        yazarlar = ki.yazarGoruntule();
        
        if (yazarlar != null)
        {
            for (KitapYazari yazar : yazarlar)
            {
                
                String cinsiyet = "";
                
                if(yazar.getYazarCinsiyet() == 0){
                    cinsiyet = "Kız";
                }
                else if (yazar.getYazarCinsiyet() == 1){
                    cinsiyet = "Erkek";
                }
                
                
                Object[] add = {yazar.getYazarid(), yazar.getYazarAdi(),yazar.getYazarSoyadi(),cinsiyet,
                    yazar.getYazarDogumTarihi(),yazar.getYazarDogumYeri(),yazar.getYazarAciklama()
                };
                
                model.addRow(add);         
            }
            
        }
        
        
    }
     
     private void search() {
        String searchTerm = searchField.getText().toLowerCase();
        TableRowSorter<DefaultTableModel> rowSorter = new TableRowSorter<>(model);
        TYazarlar.setRowSorter(rowSorter);

        int columnIndexToSearch = 1;

        if (searchTerm.length() == 0) {
            rowSorter.setRowFilter(null);
        } else {
            rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + searchTerm, columnIndexToSearch));
        }
    }
    
